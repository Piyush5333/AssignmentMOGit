package assignment1;
//This Program Calculates even number from 1 to 200
public class Even_Number 
{
   public static void main(String[] args) 
   {   
	   System.out.println("Below are Even Number Between 1 to 200 :");
	   for(int i=1;i<=200;i++)
	   {
		   if (i%2==0)
		   {
			   System.out.println(i);
		   }   
	   } 
   }
}
